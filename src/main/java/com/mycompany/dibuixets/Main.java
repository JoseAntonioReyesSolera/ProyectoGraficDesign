    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String accion = jComboBox1.getSelectedItem().toString();
        switch (accion){
            case "Text Detection":
                //do stuff
                break;
            case "Face Detection":
                //do more stuff
                break;
            case "Draw":
                if (imageFile == null) {
                    javax.swing.JOptionPane.showMessageDialog(null, "No hay imagen seleccionada", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);

                    return;
                }
                new OpenCVDrawing(imageFile);
                break;
            default :
                System.out.println("How tf you got here");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        File file = ImageFinder.showDialog((Frame) SwingUtilities.getWindowAncestor(this));
        imageFile = file;
        try {
            BufferedImage img = ImageIO.read(file);
            
            jLabel2.setText("");
            jLabel2.setIcon(new ImageIcon(img.getScaledInstance(150, 150, Image.SCALE_SMOOTH)));
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

