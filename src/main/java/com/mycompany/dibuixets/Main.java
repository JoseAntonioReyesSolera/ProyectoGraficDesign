    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String accion = jComboBox1.getSelectedItem().toString();
        if (imageFile == null) {
                    javax.swing.JOptionPane.showMessageDialog(null, "No hay imagen seleccionada", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                    return;
                }
        switch (accion){
            case "Text Detection":
                String text = TextRecognition.getTextFromImage(imageFile);
                jLabel1.setText(text);
                break;
            case "Face Detection":
                setImage(FaceDetector.detectAndSave(imageFile));
                break;
            case "Draw":
                OpenCVDrawing.setUp(imageFile, this);
                break;
            default :
                System.out.println("How tf you got here");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

